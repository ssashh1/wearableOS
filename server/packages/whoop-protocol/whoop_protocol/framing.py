def crc8(buffer: bytes) -> int:
    crc = 0
    for b in buffer:
        crc = crc8tab[crc ^ b]
    return crc


def crc32(buffer: bytes) -> int:
    return zlib.crc32(buffer) & 0xFFFFFFFF


@dataclass
class FrameCheck:
    ok: bool
    length: int | None = None
    crc8_ok: bool | None = None
    crc32_ok: bool | None = None


def verify_frame(frame: bytes) -> FrameCheck:
    """Validate a complete frame envelope and both CRCs.
    Frame: [0xAA][len u16 LE][crc8(len)][...inner...][crc32 u32 LE], total = len + 4."""
    if len(frame) < 8 or frame[0] != 0xAA:
        return FrameCheck(ok=False)
    length = struct.unpack("<H", frame[1:3])[0]
    crc8_ok = crc8(frame[1:3]) == frame[3]
    crc32_ok = None
    # length must cover at least the envelope's inner bytes (type+seq+cmd = 3 -> 7);
    # a degenerate length (e.g. 2) would slice an empty inner and make crc32(b"")==0
    # spuriously match an all-zero trailer. Leave crc32_ok=None when unvalidatable.
    if 7 <= length and length + 4 <= len(frame):
        inner = frame[4:length]
        crc32_ok = crc32(inner) == struct.unpack("<L", frame[length:length + 4])[0]
    ok = bool(crc8_ok) and bool(crc32_ok)
    return FrameCheck(ok=ok, length=length, crc8_ok=crc8_ok, crc32_ok=crc32_ok)


def frame_from_payload(data: bytes, type_byte: int, seq: int = 0, cmd: int = 0) -> bytes:
    """Reconstruct a complete frame from a bare payload (data == frame[7:]).

    Some captures store only the data portion. We rebuild the envelope
    [0xAA][len u16][crc8][type][seq][cmd][data][crc32] with a correct zlib crc32 and a
    placeholder crc8 byte (0x00) — the original header CRC isn't recoverable from a bare
    payload. Schema offsets are whole-frame with data at offset 7, so they line up.
    """
    inner = bytes([type_byte, seq, cmd]) + data
    length = len(inner) + 4
    header = bytes([0xAA]) + struct.pack("<H", length) + bytes([0x00])
    pkt = header + inner
    return pkt + struct.pack("<L", crc32(inner))


class Reassembler:
    """Accumulate BLE notification fragments into complete frames.
    A complete frame is `length + 4` bytes where length = u16 LE at buf[1:3]."""

    def __init__(self):
        self._buf = bytearray()

    def feed(self, fragment: bytes) -> list[bytes]:
        self._buf.extend(fragment)
        out: list[bytes] = []
        while True:
            sof = self._buf.find(0xAA)
            if sof == -1:
                self._buf.clear()
                break
            if sof > 0:
                del self._buf[:sof]
            if len(self._buf) < 4:
                break
            length = struct.unpack("<H", self._buf[1:3])[0]
            total = length + 4
            if len(self._buf) < total:
                break
            out.append(bytes(self._buf[:total]))
            del self._buf[:total]
        return out
